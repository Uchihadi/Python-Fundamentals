name = "Hadi Rahman"

# char is local into for loop
# Basic for loop works: prints each characters from a full name
for char in name:
    print(char)

# Arrays
name1 = ["Hadi", "Rahman"]
for char in name1:
    print(char)

# Start, Step, Stop value @ 10
# Step value default is 1
for char in range(10):
    print (char)

# (Start, Stop, Step)
for char in range(5, 10, 2):
    print (char)